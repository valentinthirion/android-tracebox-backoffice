	function getAPIPrefix()
	{
		$result = mysql_query("SELECT api_prefix FROM " . DB_PREFIX . "settings WHERE id=1") or die(mysql_error());
		$data = mysql_fetch_array($result);
		return $data['api_prefix'];
	}

	function setAPIPrefix($newPrefix)
	{
		$newPrefix = mysql_real_escape_string(htmlspecialchars($newPrefix));
		return mysql_query("UPDATE " . DB_PREFIX . "settings SET api_prefix='$newPrefix' WHERE id=1") or die(mysql_error());
	}

	function createTablesForSystem($table_prefix, $connectionPassword)
	{
		$table_prefix = htmlspecialchars($table_prefix);

		// Destinations
		if (!mysql_query("CREATE TABLE IF NOT EXISTS " . $table_prefix . "destinations (
														 id int(11) NOT NULL AUTO_INCREMENT,
														 name text NOT NULL,
														 address text NOT NULL,
														 PRIMARY KEY (id)
														 ) ENGINE=MyISAM  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1"));
	    
		if (!fillDestinationsTable($table_prefix))
			return false;
												
		if (!mysql_query("CREATE TABLE IF NOT EXISTS " . $table_prefix . "packetmodifications (
														  id int(11) NOT NULL AUTO_INCREMENT,
														  router_id int(11) NOT NULL,
														  layer text NOT NULL,
														  field text NOT NULL,
														  PRIMARY KEY (id)
														) ENGINE=MyISAM  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1"))
														 return false;

	    if (!mysql_query("CREATE TABLE IF NOT EXISTS " . $table_prefix . "probes (
														  id int(11) NOT NULL AUTO_INCREMENT,
														  destination text NOT NULL,
														  starttime int(11) NOT NULL,
														  endtime int(11) NOT NULL,
														  connectivityMode int(11) NOT NULL,
														  location text NOT NULL,
														  PRIMARY KEY (id)
														) ENGINE=MyISAM  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1"))
														 return false;

		if (!mysql_query("CREATE TABLE IF NOT EXISTS " . $table_prefix . "routers (
														  id int(11) NOT NULL AUTO_INCREMENT,
														  probe_id int(11) NOT NULL,
														  address text NOT NULL,
														  ttl int(11) NOT NULL,
														  PRIMARY KEY (id)
														) ENGINE=MyISAM  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1"))
														 return false;

		if (!mysql_query("CREATE TABLE IF NOT EXISTS " . $table_prefix . "settings (
														  id int(11) NOT NULL,
														  password text NOT NULL
														) ENGINE=MyISAM DEFAULT CHARSET=utf8;"))
														 return false;

		$hash = hash('sha512', $connectionPassword);
		if (!mysql_query("INSERT INTO " . $table_prefix . "settings
														(id, password) VALUES
														(1, '$hash')"))
														return false;

		return true;									 										
	}

